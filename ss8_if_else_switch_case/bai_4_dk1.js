let a = +prompt(" Nhập vào số thứ a ");
let b = +prompt(" Nhập vào số thứ b ");
let c = +prompt(" Nhập vào số thứ c ");
if (a >= b && a >= c) {
    alert(` ${a}  là A so lớn nhất `)
} else if (b >= a && b >= c){
    alert(` ${b}  là B so lớn nhất `)
} else {
    alert(` ${c}  là C so lớn nhất `)
}


